"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também com tipo vazio, porém, falar quie é um tipo sem tipo é mais apropriado

OBS: O tipo None é sempre especificado com a primeira letra maiúscula.

Quando utilizar?

- Podemos utilizar quando queremos criar uma variável e inicializá-lá com um tipo sem tipo, antes de receber um valor especifico.
"""

numeros = None;

print(numeros);
print(type(numeros));